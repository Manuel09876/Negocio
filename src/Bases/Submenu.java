
package Bases;


public class Submenu {
    private int id;
    private int menuId;
    private String nombreSubmenu;
    private String rutaSubmenu;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getMenuId() {
        return menuId;
    }

    public void setMenuId(int menuId) {
        this.menuId = menuId;
    }

    public String getNombreSubmenu() {
        return nombreSubmenu;
    }

    public void setNombreSubmenu(String nombreSubmenu) {
        this.nombreSubmenu = nombreSubmenu;
    }

    public String getRutaSubmenu() {
        return rutaSubmenu;
    }

    public void setRutaSubmenu(String rutaSubmenu) {
        this.rutaSubmenu = rutaSubmenu;
    }
    
    
}
